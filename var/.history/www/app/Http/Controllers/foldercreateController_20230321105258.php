        $folder->title = $request->title;

        // ユーザーに紐づけて保存（ユーザー->リレーション->保存(インスタンスした変数)
        Auth::user()->folders()->save($folder);

        $id = $folder->id;

        return  redirect()->route('list.index', [
            'id' => $folder->id,
        ]);
    }

